
package com.ulp;


public abstract class EntidadJuego {
    
    protected int x;
    protected int y;

    public EntidadJuego() {
    }

    public EntidadJuego(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
    
    
    
    
    
    
    
    
    
}
